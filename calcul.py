def somma() :
    """questa funzione restituisce la somma di due numeri inseriti dall'utente"""
    a = float(input("Inserisci il primo numero: "))
    b = float(input("Inserisci il secondo numero: "))
    return a + b
def produit() :
    """questa funzione restituisce il prodotto di due numeri inseriti dall'utente"""
    a = float(input("Inserisci il primo numero: "))
    b = float(input("Inserisci il secondo numero: "))
    return a * b
    